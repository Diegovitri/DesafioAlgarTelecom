package model;

public class JsonResponse {
	private Integer status;
	
	public JsonResponse(){
		
	}
	
	public JsonResponse(Integer status) {
		super();
		this.status = status;
	}


	public Integer getStatus() {
		return status;
	}

	public void setStatus(Integer status) {
		this.status = status;
	}
	


	

	
}
